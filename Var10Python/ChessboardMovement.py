def check_movement(N, M, s):
    x = 0  # начальная позиция по горизонтали
    y = 0  # начальная позиция по вертикали

    for move in s:  # перебор всех ходов в последовательности
        if move == 'L':  # если ход влево
            if x < N / 2 and x > -(N / 2):  # если новая позиция по горизонтали находится в пределах доски
                x -= 1  # двигаемся влево
            else:
                return 'No'  # выход за пределы доски
        elif move == 'R':  # если ход вправо
            if x < N / 2 and x > -(N / 2):  # если новая позиция по горизонтали находится в пределах доски
                x += 1  # двигаемся вправо
            else:
                return 'No'  # выход за пределы доски
        elif move == 'U':  # если ход вверх
            if y < M / 2 and y > -(M / 2):  # если новая позиция по вертикали находится в пределах доски
                y += 1  # двигаемся вверх
            else:
                return 'No'  # выход за пределы доски
        elif move == 'D':  # если ход вниз
            if y < M / 2 and y > -(M / 2):  # если новая позиция по вертикали находится в пределах доски
                y -= 1  # двигаемся вниз
            else:
                return 'No'  # выход за пределы доски
        else:
            return 'Invalid Command'  # некорректная команда

    return f"({x},{y})"  # возвращаем координаты

# Пример использования функции
N1, M1 = map(int, input("Введите N и M: ").split())  # ввод размеров доски
s1 = input("Введите последовательность ходов: ")  # ввод последовательности ходов
print(check_movement(N1, M1, s1))  # вывод результата
